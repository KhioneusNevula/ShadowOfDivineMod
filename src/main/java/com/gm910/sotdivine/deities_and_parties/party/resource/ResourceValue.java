package com.gm910.sotdivine.deities_and_parties.party.resource;

/**
 * Different types of values resources can have
 * 
 * @author borah
 *
 */
public enum ResourceValue {
	/**
	 * How much "empowerment" this resource gives a deity
	 */
	DEITY_EMPOWERING,
	/**
	 * How important this resource is to survival
	 */
	SURVIVAL,
	/**
	 * How much status this resource symbolizes
	 */
	MORTAL_STATUS
}
